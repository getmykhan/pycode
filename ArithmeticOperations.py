"""
Arithmetic Operations!
"""

def add(a, b):
    sum1 = a + b
    return sum1


def sub(a, b):
    sub1 = a - b
    return sub1

def mul(a, b):
    mul1 = a * b
    return mul1

def div(a, b):
    div1 = a // b
    return div1

print(add(20, 10))
print(sub(20, 10))
print(mul(20, 10))
print(div(20, 10))
